int		ft_iterative_factorial(int nb)
{
	int x;

	x = 1;
	if ((nb < 0) || (nb > 12))
		return (0);
	if ((nb == 1) || (nb == 0))
		return (1);
	while (0 < nb)
	{
		x *= nb;
		nb--;
	}
	return (x);
}
