int		ft_sqrt(int nb)
{
	int x;
	int sqrt;

	x = 1;
	sqrt = 0;
	while (x <= nb / 2)
	{
		sqrt = x * x;
		if (sqrt == nb)
		{
			return (x);
		}
		x = x + 1;
	}
	return (0);
}
